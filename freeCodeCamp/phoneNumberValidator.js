function telephoneCheck(str) {
    const PHONES = [/1\d{10}/, /^\d{10}$/, /^(\d{3}-){2}\d{4}$/, /^\(\d{3}\)\s?\d{3}[-\s]?\d{4}$/, /^1[\s-]?\d{3}[\s-]?\d{3}[-\s]?\d{4}$/, /^1[\s-]?\(\d{3}\)[\s-]?\d{3}[-\s]?\d{4}$/]
    return PHONES.some(el => el.test(str));
}

